
// program, który odlicza od 100 do 0 przy pomocy pętli WHILE

package czesc3;

public class LiczbyParzyste_0_100 {
    public static void main(String[] args) {
        
        int i = -1;
        int j = 100;
        
        
        
        while (i++ <100){ 
            System.out.println(j--);
        }
           
        };
        
        
    }
