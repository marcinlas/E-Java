
// program, który odlicza od 10 do 0 przy pomocy pętli WHILE

package czesc3;

public class Cwiczenie_While {
    
    public static void main(String[] args) {
        
        int i = 10;
                        
         while (i >= 0) {
             System.out.println(i--);
                }
        }       
     }
