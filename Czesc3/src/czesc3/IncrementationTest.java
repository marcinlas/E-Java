
package Czesc3;

public class IncrementationTest {
    public static void main(String[] args) {
        
        int a = 1;
        
        System.out.println("a = " + a);
        System.out.println("a++: " + a++);
        System.out.println("a = " + a);
        System.out.println("++a: " + ++a);
        System.out.println("a = " + a);
        
    }
    
}